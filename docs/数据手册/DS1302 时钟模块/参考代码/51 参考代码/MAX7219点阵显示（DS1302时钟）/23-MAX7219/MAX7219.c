// 写入命令和数据
void MAX7219_Write(unsigned char Add, Data)
{
	unsigned char i;
	MAX7219_CS = 0;
	for (i = 0; i < 8; i++)
	{
		MAX7219_CLOK = 0;
		MAX7219_DIN = Add & (0x80 >> i);
		MAX7219_CLOK = 1;

	}
	
	for (i = 0; i < 8; i++)
	{
		MAX7219_CLOK = 0;
		MAX7219_DIN = Data & (0x80 >> i);
		MAX7219_CLOK = 1;
	}
	
}

// 初始化
void MAX7219_Init()
{
	unsigned char i;
	MAX7219_CS = 0;
	for (i = 0; i < LEDNUMBER; i++)
	{
		MAX7219_Write(MODE, 0x00);   // 译码方式 (1为BCD译码, 0为不译码)
	}
	MAX7219_CS = 1;

	MAX7219_CS = 0;
	for (i = 0; i < LEDNUMBER; i++)
	{
		MAX7219_Write(BRIGHT, BrightSet); // 亮度设置 (0x00~0x0F, 0x01最暗，0x0F最亮)
	}
	MAX7219_CS = 1;

	MAX7219_CS = 0;
	for (i = 0; i < LEDNUMBER; i++)
	{
		MAX7219_Write(SCANNING, 0x07); // 扫描位数 (0x00~0x07, 选择扫描1-8个LED)
	} 
	MAX7219_CS = 1;

	MAX7219_CS = 0;
	for (i = 0; i < LEDNUMBER; i++)
	{
		MAX7219_Write(STANDBY, 0x01); // 待机模式 (1为正常工作模式, 0为停机模式，显示熄灭但数据不消失)
	}
	MAX7219_CS = 1;

	MAX7219_CS = 0;
	for (i = 0; i < LEDNUMBER; i++)
	{
		MAX7219_Write(DISTEST, 0x00); // 显示测试 (0为按数据正常显示，1为测试模式,即LED全亮)
	}
	MAX7219_CS = 1;	
}


// 更新显示
void MAX7219_Display()
{
	unsigned char i, j;

	for (j = 0; j < 8; j++)
	{
		MAX7219_CS = 0;
		for (i = 0; i < LEDNUMBER; i++)
		{
			MAX7219_Write(j+1, DisplayData[i][j]);
		}
		MAX7219_CS = 1;
		Delay1ms(15); // 一定延时可以让更新数字时带有类似平滑的效果
	}
	
}
